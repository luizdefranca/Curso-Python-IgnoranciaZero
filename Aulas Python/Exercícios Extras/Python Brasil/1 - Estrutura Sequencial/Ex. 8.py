"""
Faça um Programa que pergunte quanto você ganha por hora
e o número de horas trabalhadas no mês.
Calcule e mostre o total do seu salário no referido mês
"""
ganho = int(input("Digite o quanto você ganha por hora: "))
horas = int(input("Digite o numero de horas trabalhadas neste mês: "))
print("O seu salário será R$", ganho*horas)
